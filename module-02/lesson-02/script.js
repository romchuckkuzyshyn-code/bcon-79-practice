const btn14 = document.querySelector(".b-14");
const out14 = document.querySelector(".out-14");

btn14.onclick = fn14;

function fn14() {
  const elem = document.querySelectorAll(".i-14");
  let result = "";
  for (let i = 0; i < elem.length; i++) {
    if (elem[i].checked) {
      result += elem[i].value;
    }
  }
  out14.textContent = result;
}
